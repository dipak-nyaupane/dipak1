function ClearAll() {
   const list = document.getElementsByClassName("hidethis");
   var i;
   for (i = 0; i < list.length; i++) {
      list[i].style.display = 'none';
   } 
}

function ClearDefinitionAll(TitleID) {
   const list = document.getElementsByClassName(TitleID);
   var i;
   for (i = 0; i < list.length; i++) {
      list[i].style.display = 'none';
   } 
}

